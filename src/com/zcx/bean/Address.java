package com.zcx.bean;



public class Address {
		
		private int address_id;
		
		private int address_user_id;
		
		private String address_name;
		
		private String address_phone;
		
		private String address_province;
		
		private String address_city;
		
		private String address_district;
		
		private String address_detial;

		public int getAddress_id() {
			return address_id;
		}

		public void setAddress_id(int address_id) {
			this.address_id = address_id;
		}

		public int getAddress_user_id() {
			return address_user_id;
		}

		public void setAddress_user_id(int address_user_id) {
			this.address_user_id = address_user_id;
		}

		public String getAddress_name() {
			return address_name;
		}

		public void setAddress_name(String address_name) {
			this.address_name = address_name;
		}

		public String getAddress_phone() {
			return address_phone;
		}

		public void setAddress_phone(String address_phone) {
			this.address_phone = address_phone;
		}

		public String getAddress_province() {
			return address_province;
		}

		public void setAddress_province(String address_province) {
			this.address_province = address_province;
		}

		public String getAddress_city() {
			return address_city;
		}

		public void setAddress_city(String address_city) {
			this.address_city = address_city;
		}

		

		public String getAddress_district() {
			return address_district;
		}

		public void setAddress_district(String address_district) {
			this.address_district = address_district;
		}

		public String getAddress_detial() {
			return address_detial;
		}

		public void setAddress_detial(String address_detial) {
			this.address_detial = address_detial;
		}

		
		
		
		

}
