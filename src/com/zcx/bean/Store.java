package com.zcx.bean;

public class Store {
	private int store_id;
	private String store_name;
	private int store_address;
	private int store_owner;
	private int store_account;
	private int store_area;
	public int getStore_id() {
		return store_id;
	}
	public void setStore_id(int store_id) {
		this.store_id = store_id;
	}
	public String getStore_name() {
		return store_name;
	}
	public void setStore_name(String store_name) {
		this.store_name = store_name;
	}
	public int getStore_address() {
		return store_address;
	}
	public void setStore_address(int store_address) {
		this.store_address = store_address;
	}
	public int getStore_owner() {
		return store_owner;
	}
	public void setStore_owner(int store_owner) {
		this.store_owner = store_owner;
	}
	public int getStore_account() {
		return store_account;
	}
	public void setStore_account(int store_account) {
		this.store_account = store_account;
	}
	public int getStore_area() {
		return store_area;
	}
	public void setStore_area(int store_area) {
		this.store_area = store_area;
	}
	
	
	
	
}
